kanta = input("Mikä on suorakulmion kanta")
korkeus = input("Mikä on suorakulmion korkeus")
kanta_luku = float(kanta)
korkeus_luku = float(korkeus)
pinta_ala = kanta_luku * korkeus_luku
piiri = kanta_luku + kanta_luku + korkeus_luku + korkeus_luku
print("Suorakulmion pinta-ala on")
print(pinta_ala)
print("Suorakulmion piiri on")
print(piiri)

